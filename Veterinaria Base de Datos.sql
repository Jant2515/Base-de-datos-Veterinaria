CREATE TABLE AUDITORIA (
ID_RECORD NUMBER GENERATED ALWAYS AS IDENTITY,
ACCION VARCHAR2(200),
USUARIO VARCHAR2(200),
FECHA DATE,
PRIMARY KEY (ID_RECORD)
);

CREATE OR REPLACE TRIGGER PISTADEAUDITORIA
AFTER INSERT OR DELETE OR UPDATE ON CITA
DECLARE 
        VUSUARIO  VARCHAR2(20);
        VACCION VARCHAR2(200);
        VFECHA DATE;
BEGIN
        SELECT USER,SYSDATE INTO VUSUARIO,VFECHA FROM DUAL;
    IF INSERTING THEN 
        VACCION := 'DATO INSERTADO';
    ELSIF UPDATING THEN 
        VACCION:='SE ACTUALIZÓ UN DATO';
    ELSIF DELETING THEN 
        VACCION:='SE ELIMINÓ';
    END IF;
        INSERT INTO AUDITORIA (USUARIO,ACCION,FECHA) VALUES(VUSUARIO,VACCION,VFECHA);
END;
