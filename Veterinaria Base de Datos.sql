CREATE OR REPLACE PROCEDURE CitaFecha (vFecha cita.FECHA_CITA%type)
as
vnombre cita.nombre_mascota_cita%type;
vhora cita.hora_cita%type;
vservicio cita.servicio_cita%type;
CURSOR  DATOS IS SELECT nombre_mascota_cita,hora_cita,servicio_cita from cita
                 WHERE FECHA_CITA=vFecha
                 ORDER BY 1;
BEGIN
  OPEN DATOS;
  LOOP 
    FETCH DATOS INTO vnombre,vhora,vservicio;
    EXIT WHEN DATOS%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE('Nombre de la mascota: ' || vnombre || '     A las: ' || vhora ||'    Tipo de servicio: '||vservicio);
  END LOOP;
  CLOSE DATOS;
END;

EXECUTE CitaFecha('20/09/21');


