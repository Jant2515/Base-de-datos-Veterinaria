CREATE OR REPLACE PROCEDURE IngresarCliente (vcedula CLIENTE.CEDULA%type)
as
vcedula CLIENTE.CEDULA%type;
vnombre CLIENTE.NOMBRECLIENTE%type;
vapellido CLIENTE.APELLIDOCLIENTE%type;
vtelefono CLIENTE.TELEFONO%type;
vmail CLIENTE.EMAILCLIENTE% type;
CURSOR  DATOS is select CEDULA,NOMBRECLIENTE,APELLIDOCLIENTE,TELEFONO,EMAILCLIENTE from cliente
                 where CEDULA= vcedula
                 order by 1;
BEGIN
  OPEN DATOS;
  LOOP 
