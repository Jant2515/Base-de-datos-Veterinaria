    EXIT when DATOS%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE('Cedula: ' || vcedula ||   'Nombre: ' || vnombre || '     apellido: ' || vapellido ||'    Telefono: '||vtelefono||  '  Mail:'  ||vmail);
  END LOOP;
  CLOSE DATOS;
END;

